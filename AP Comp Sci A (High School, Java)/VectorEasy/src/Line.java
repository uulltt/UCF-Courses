
public class Line {
	private int x1, x2, y1, y2;
	public Line(int sx, int sy, int fx, int fy){
		x1 = sx;
		y1 = sy;
		x2 = fx;
		y2 = fy;
	}
	
	public int getX1(){
		return x1;
	}
	
	public int getX2(){
		return x2;
	}
	
	public int getY1(){
		return y1;
	}
	
	public int getY2(){
		return y2;
	}

}
